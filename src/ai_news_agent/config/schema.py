def summarise_agent_config(config: AgentConfig) -> str:
    """
    Return a human-readable summary of a validated agent config.

    Used by the CLI validator to confirm what was parsed.

    Traces: SRC-072 (config visibility)
    """
    cadence_overrides = (
        ", ".join(f"{k}={v.model}" for k, v in config.llm.cadence_overrides.items()) or "none"
    )
    handle_list = (
        ", ".join(f"@{h.handle}(×{h.weight})" for h in config.twitter.handles[:5])
        or "none configured"
    )
    if len(config.twitter.handles) > 5:
        handle_list += f" … +{len(config.twitter.handles) - 5} more"

    tier_counts = {
        "1a (custom)": len(config.sources.custom),
        "1b (business press)": len(config.sources.tier_1b),
        "2 (AI blogs)": len(config.sources.tier_2),
        "3 (tech press)": len(config.sources.tier_3),
        "4 (policy/research)": len(config.sources.tier_4),
    }
    tiers_str = ", ".join(
        f"Tier {tier}: {count}" for tier, count in tier_counts.items() if count > 0
    )

    return (
        f"\n{'─' * 60}\n"
        f"  Agent ID   : {config.agent_id}\n"
        f"  LLM        : {config.llm.provider} / {config.llm.model}\n"
        f"  Overrides  : {cadence_overrides}\n"
        f"  Prompt dir : {config.curation_prompt}\n"
        f"  Output dir : {config.output_dir}\n"
        f"  Limits     : daily={config.limits.daily_top_n}, "
        f"weekly={config.limits.weekly_top_n}, "
        f"monthly={config.limits.monthly_top_n}, "
        f"annual={config.limits.annual_top_n}\n"
        f"  Sources    : {tiers_str or 'none configured'}\n"
        f"  Twitter    : {'enabled' if config.twitter.enabled else 'disabled'} — {handle_list}\n"
        f"{'─' * 60}\n"
    )


