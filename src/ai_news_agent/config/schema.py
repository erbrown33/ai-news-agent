# Runtime validation helpers
# ---------------------------------------------------------------------------


class SchemaValidationError(Exception):
    """
    Raised when a YAML document fails JSON Schema or Pydantic validation.
    Contains structured error details for display to the user.

    Traces: SRC-071 (fail loudly with actionable messages)
    """

    def __init__(self, errors: list[str], source: str = "<unknown>") -> None:
        self.errors = errors
        self.source = source
        super().__init__(
            f"Configuration validation failed for {source}:\n"
            + "\n".join(f"  • {e}" for e in errors)
        )


def validate_agent_yaml(
    path: str | Path,
    *,
    strict: bool = False,
) -> AgentConfig:
    """
    Fully validate an agent YAML file and return the parsed :class:`AgentConfig`.

    Performs:
    1. YAML parse check.
    2. Pydantic schema validation (produces typed AgentConfig).
    3. Optionally: ``strict=True`` raises on any extra fields.

    This function is primarily for the CLI validator and pre-commit hooks.
    Production code should use :func:`ai_news_agent.config.loader.load_agent_config`.

    Args:
        path:   Path to the agent YAML file.
        strict: If True, raise on extra/unrecognised fields in the YAML.

    Returns:
        Validated :class:`AgentConfig`.

    Raises:
        :class:`SchemaValidationError`: On any validation failure.
    """
    from ai_news_agent.config.loader import ConfigError, validate_no_secrets_in_yaml

    resolved = Path(path)
    errors: list[str] = []

    if not resolved.exists():
        raise SchemaValidationError(
            [f"File not found: {resolved.absolute()}"], source=str(resolved)
        )

    raw_text = resolved.read_text(encoding="utf-8")

    # Secret scan (SRC-073)
    try:
        validate_no_secrets_in_yaml(raw_text, source_name=str(resolved))
    except ConfigError as exc:
        errors.append(str(exc))

    # YAML parse
    try:
        data = yaml.safe_load(raw_text) or {}
    except yaml.YAMLError as exc:
        raise SchemaValidationError([f"YAML parse error: {exc}"], source=str(resolved)) from exc

    # Pydantic validation
    from pydantic import ValidationError

    try:
        config = AgentConfig.model_validate(data)
    except ValidationError as exc:
        for error in exc.errors():
            loc = " → ".join(str(p) for p in error["loc"])
            errors.append(f"[{loc}] {error['msg']}")

    if errors:
        raise SchemaValidationError(errors, source=str(resolved))

    return config  # type: ignore[return-value]


