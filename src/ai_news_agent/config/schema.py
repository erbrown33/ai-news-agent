def _cli_main(argv: list[str] | None = None) -> int:
    """
    CLI validator and schema exporter.

    Commands::

        # Export JSON Schema files to configs/
        python -m ai_news_agent.config.schema --export-dir configs/

        # Validate a specific agent YAML
        python -m ai_news_agent.config.schema --validate configs/default-agent.yaml

        # Validate the scheduler YAML
        python -m ai_news_agent.config.schema --validate configs/scheduler.yaml --type scheduler

        # Validate and show config summary
        python -m ai_news_agent.config.schema --validate configs/default-agent.yaml --summary

    Exit codes: 0 = success, 1 = validation failure, 2 = argument error.

    Traces: SRC-071 (clear error output), SRC-072 (multi-config support)
    """
    parser = argparse.ArgumentParser(
        prog="ai_news_agent.config.schema",
        description=(
            "AI News Agent config validator and JSON Schema exporter.\n\n"
            "Examples:\n"
            "  python -m ai_news_agent.config.schema --export-dir configs/\n"
            "  python -m ai_news_agent.config.schema --validate configs/default-agent.yaml\n"
            "  python -m ai_news_agent.config.schema --validate configs/scheduler.yaml "
            "--type scheduler"
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )

    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument(
        "--export-dir",
        metavar="DIR",
        help=(
            "Export JSON Schema files to DIR. "
            "Writes agent-config.schema.json and scheduler.schema.json."
        ),
    )
    mode.add_argument(
        "--validate",
        metavar="PATH",
        nargs="+",
        help="One or more YAML config files to validate.",
    )

    parser.add_argument(
        "--type",
        choices=["agent", "scheduler"],
        default="agent",
        help="Config type when using --validate (default: agent).",
    )
    parser.add_argument(
        "--summary",
        action="store_true",
        help="Print a human-readable summary of the validated config.",
    )
    parser.add_argument(
        "--json-schema",
        action="store_true",
        help=("When used with --validate, also print the JSON Schema to stdout for reference."),
    )

    args = parser.parse_args(argv)

    # ── Export mode ──────────────────────────────────────────────────────────
    if args.export_dir:
        export_schemas(args.export_dir)
        return 0

    # ── Validate mode ─────────────────────────────────────────────────────────
    paths = [Path(p) for p in args.validate]
    config_type = args.type

    if args.json_schema:
        schema = generate_agent_schema() if config_type == "agent" else generate_scheduler_schema()
        print(json.dumps(schema, indent=2))
        print()

    exit_code = 0
    for path in paths:
        try:
            if config_type == "agent":
                config = validate_agent_yaml(path)
                print(f"✓ {path} — valid agent configuration")
                if args.summary:
                    print(summarise_agent_config(config))
            else:
                sched = validate_scheduler_yaml(path)
                enabled = sum(1 for a in sched.agents if a.enabled)
                print(
                    f"✓ {path} — valid scheduler configuration "
                    f"({len(sched.agents)} agents registered, {enabled} enabled)"
                )
                if args.summary:
                    print(f"  Agents: {', '.join(a.id for a in sched.agents)}")
                    print(f"  Retries: {sched.scheduler.max_retries}")
                    print(f"  Backoff: {sched.scheduler.retry_backoff_base_seconds}s base")

        except SchemaValidationError as exc:
            print(f"✗ Validation FAILED for {path}:", file=sys.stderr)
            for error in exc.errors:
                print(f"  • {error}", file=sys.stderr)
            exit_code = 1

    return exit_code


if __name__ == "__main__":
    sys.exit(_cli_main())
