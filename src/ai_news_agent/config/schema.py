"""
config/schema.py — JSON Schema generation, export, and YAML-against-schema validation.

This module serves three purposes:

1. **Schema generation** — derives JSON Schema from Pydantic v2 models so that
   external tools (IDE YAML plugins, pre-commit hooks, CI validators) can
   validate configs without importing the full Python package.

2. **Schema export** — writes ``configs/agent-config.schema.json`` and
   ``configs/scheduler.schema.json`` on demand (or from CI).

3. **Runtime YAML validation** — validates a YAML dict against the schema before
   handing it to Pydantic, giving clearer error messages for simple mistakes.

Traces: SRC-071 (fail loudly with clear errors), SRC-072 (per-agent YAML schemas),
        SRC-073 (schema must forbid secrets fields)

CLI usage::

    python -m ai_news_agent.config.schema --export-dir configs/
    python -m ai_news_agent.config.schema --validate configs/default-agent.yaml
    python -m ai_news_agent.config.schema --validate configs/scheduler.yaml --type scheduler
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

import yaml

from ai_news_agent.config.models import AgentConfig, SchedulerConfig

# ---------------------------------------------------------------------------
# Schema generation
# ---------------------------------------------------------------------------


def generate_agent_schema() -> dict[str, Any]:
    """
    Generate a JSON Schema dict from the :class:`AgentConfig` Pydantic model.

    The schema is generated by Pydantic v2's ``model_json_schema()`` and
    then enriched with:
    - A ``$schema`` declaration (Draft 7).
    - A ``title`` and ``description`` block documenting the schema's purpose.
    - A ``$comment`` pointing to the implementation plan for traceability.

    Traces: SRC-071–SRC-073 (config validation contract)

    Returns:
        A JSON-serialisable dict representing the full JSON Schema.
    """
    raw_schema = AgentConfig.model_json_schema()

    # Enrich with metadata
    raw_schema.setdefault("$schema", "http://json-schema.org/draft-07/schema#")
    raw_schema["title"] = "AI News Agent — Per-Agent Configuration Schema"
    raw_schema["description"] = (
        "JSON Schema for a per-agent YAML configuration file. "
        "Validated by load_agent_config() at startup. "
        "Secrets (API keys, tokens) must NEVER appear in this file — use env vars. "
        "Requirement traces: SRC-017–SRC-021, SRC-029–SRC-032, SRC-036–SRC-046, "
        "SRC-054, SRC-057, SRC-071–SRC-073, SRC-113, SRC-129, SRC-145."
    )
    raw_schema["$comment"] = (
        "Generated from ai_news_agent.config.models.AgentConfig. "
        "See docs/requirements/implementation-plan.md (SLICE-003) for traceability. "
        "Do not edit manually — regenerate with: "
        "python -m ai_news_agent.config.schema --export-dir configs/"
    )

    return raw_schema


def generate_scheduler_schema() -> dict[str, Any]:
    """
    Generate a JSON Schema dict from the :class:`SchedulerConfig` Pydantic model.

    Traces: SRC-052, SRC-072, SRC-144, SRC-147

    Returns:
        A JSON-serialisable dict representing the full JSON Schema.
    """
    raw_schema = SchedulerConfig.model_json_schema()

    raw_schema.setdefault("$schema", "http://json-schema.org/draft-07/schema#")
    raw_schema["title"] = "AI News Agent — Scheduler Configuration Schema"
    raw_schema["description"] = (
        "JSON Schema for configs/scheduler.yaml. "
        "Defines retry policy, cron triggers, API settings, and agent registry. "
        "Requirement traces: SRC-052, SRC-072, SRC-144, SRC-147."
    )
    raw_schema["$comment"] = (
        "Generated from ai_news_agent.config.models.SchedulerConfig. "
        "Regenerate with: python -m ai_news_agent.config.schema --export-dir configs/"
    )

    return raw_schema


# ---------------------------------------------------------------------------
# Schema export
# ---------------------------------------------------------------------------


def export_schemas(export_dir: str | Path = "configs") -> None:
    """
    Write JSON Schema files to ``export_dir``.

    Files written:
    - ``{export_dir}/agent-config.schema.json``
    - ``{export_dir}/scheduler.schema.json``

    These files are designed to be checked in alongside YAML configs so that
    IDE plugins (e.g. VS Code YAML extension with ``yaml.schemas`` setting)
    can provide inline validation and autocompletion.

    Traces: SRC-071 (schema enables fast early validation)

    Args:
        export_dir: Directory where schema files will be written.
                    Created if it does not exist.
    """
    out_dir = Path(export_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    agent_path = out_dir / "agent-config.schema.json"
    scheduler_path = out_dir / "scheduler.schema.json"

    agent_schema = generate_agent_schema()
    scheduler_schema = generate_scheduler_schema()

    agent_path.write_text(
        json.dumps(agent_schema, indent=2, sort_keys=False) + "\n",
        encoding="utf-8",
    )
    scheduler_path.write_text(
        json.dumps(scheduler_schema, indent=2, sort_keys=False) + "\n",
        encoding="utf-8",
    )

    print(f"✓ Wrote agent schema  → {agent_path}")
    print(f"✓ Wrote scheduler schema → {scheduler_path}")


